"""
Operadores dee comparação (relacionais)
OP      Significado          Exemplo (True)
>       Maior                2 > 1
>=      Maior ou igual       2 >= 2
<       Menor                1 < 2
<=      Menor ou igual       2 <= 2
==      Igual                'a' == 'a'
!=      Diferente            'a' != 'b'
"""
maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(diferente)
print(igual)
print(menor_ou_igual)
print(menor)
print(maior_ou_igual)
print(maior)

