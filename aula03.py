"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
Tipagem Dinâmica = O Python já sabe o tipo de informação que você está passando.
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

#Aspas simples
print('Kauã Lasco')

#Aspas duplas
print("Kauã Lasco")

#Escape
#serve para pular o próximo caractere e não o ler
print("Kauã \"Lasco\"")

#r
#é utilizado quando você quer exibir o caractere de Escape
print(r"Kauã \"Lasco\"")

#Para utilizar aspas dentro de uma string de forma mais simples, 
# você pode simplesmente inverter as aspas desejadas das aspas de abertura da string.

#Aspas duplas dentro da string
print('Kauã "Lasco"')

#Aspas simples dentro da string
print("Kauã 'Lasco'")

"""Uma Docstring também é considerada um tipo de string!"""