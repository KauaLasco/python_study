# if / elif      / else
# se / se não se / se não
entrada = input('Você quer "Entrar" ou "Sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema!')
elif entrada == 'sair':
    print('Você saiu do sistema!')
else:
    print('Você não digitou "entrar" nem "sair"!')

#Como podemos ver o if, elif e else são blocos de códigos condicionais
#para executar uma tarefa específica, criando condições para ela!

#Um bloco condicional sempre se inicia com "if", continua com "elif" e termina com "else".

#Em um bloco condicinal você pode ter um somente um "if" e um "else"!

#A regra sequêncial dos blocos condicionais é lei!

#O "elif" é o único que pode se repetir dentro de um bloco condicional!