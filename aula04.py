#Tipos int e float
#int -> Número inteiro
#O tipo int representa qualquer número
#positivo ou negativo. int sem sinal é considerado
#positivo
print(11) #int
print(-11) #int
print(0) #int
#Esses são números literais inteiros, pois estamos literalmente
#os escrevendo na tela.

#float -> Número com ponto flutuante
#O tipo float representa qualquer número
#positivo ou negativo com ponto flutuante.
#float sem sinal é considerado positivo.
print(1.1, 10.11) #float
print(2.2) #float
print(-1.5) #float

#A função (que na verdade é uma classe, mas começa com a letra minúscula) type mostra o tipo que o Python
#inferiu ao valor.
print(type('Kauã'))
print(type(22))
print(type(-11))
print(type(1.7))
print(type(-7.7))

